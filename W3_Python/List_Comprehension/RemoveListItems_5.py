# Remove Specified Items: remove()

l1 = ["apple","banana","cherry"]
print(l1)
l1.remove("banana")
print(l1)

l2 = [10,11,14,15,13,10]
print(l2)
l2.remove(10)
print(l2)

# Remove Specified Index: pop(), del

l2=["Ram","Shyam","Krishna"]
